package Model;

import java.sql.Date;

/**
 *
 * @author Admin
 */
public class Blog {

    private int id;
    private String name;
    private String picture;
    private String content;
    private Date date;

    public Blog(int id, String name, String picture, String content, Date date) {
        this.id = id;
        this.name = name;
        this.picture = picture;
        this.content = content;
        this.date = date;
    }

    public Blog() {
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getPicture() {
        return picture;
    }

    public void setPicture(String picture) {
        this.picture = picture;
    }

    public String getContent() {
        return content;
    }

    public void setContent(String content) {
        this.content = content;
    }

    public Date getDate() {
        return date;
    }

    public void setDate(Date date) {
        this.date = date;
    }

}
